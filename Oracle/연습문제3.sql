SELECT A.ACC_NO, A.ACC_CID, A.ACC_NAME, C.C_NAME, 
    A.ACC_BALANCE +
        SUM(CASE WHEN t.TR_TYPE = 001 THEN t.TR_AMOUNT ELSE 0 END) -
        SUM(CASE WHEN t.TR_TYPE = 002 THEN t.TR_AMOUNT ELSE 0 END) "최종잔액"
    FROM ACCOUNT A
    LEFT JOIN TRANSACTION t ON A.ACC_NO = t.TR_ACC
    JOIN CUSTOMER C ON A.ACC_CID = C.C_ID
    WHERE C_TYPE = 1
    GROUP BY
        A.ACC_NO
        ,A.ACC_CID
        ,A.ACC_NAME
        ,A.ACC_BALANCE
        ,C.C_NAME
    ORDER BY A.ACC_NO;


-- ■■■■■ 실습3-30
SELECT 
    C.C_NAME,
    A.ACC_NO,
    T.TOTAL_DEPOSIT,
    C.C_ADDR
FROM ACCOUNT A
    JOIN CUSTOMER C ON A.ACC_CID = C.C_ID
    JOIN (
            SELECT TR_ACC, SUM(TR_AMOUNT) AS TOTAL_DEPOSIT
            FROM TRANSACTION
            WHERE TR_TYPE = 1
            GROUP BY TR_ACC
            )
        T ON A.ACC_NO = T.TR_ACC
    WHERE
        T.TOTAL_DEPOSIT = (
            SELECT MAX(SUM_AMOUNT)
            FROM (
                SELECT
                    TR_ACC, SUM(TR_AMOUNT) AS SUM_AMOUNT
                    FROM TRANSACTION
                    WHERE TR_TYPE = 1
                    GROUP BY TR_ACC
                )
        );


